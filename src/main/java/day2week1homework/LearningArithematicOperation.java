package day2week1homework;

public class LearningArithematicOperation {

	public static void main(String[] args) {
	int numberone=22;
	int numbertwo=44;
	
	int sum=numberone+numbertwo;
	int difference=numberone-numbertwo;
	int product=numberone*numbertwo;
	int quotient=numberone/numbertwo;
	int reminder=numberone%numbertwo;
	
	System.out.println("The Sum of the Number: "+sum);
	System.out.println("The Difference of the Number: "+difference);
	System.out.println("The Product of the Number: "+product);
	System.out.println("The Quotient of the Number: "+quotient);
	System.out.println("The Reminder of the Number: "+reminder);
	System.out.println("44/2: "+numberone/2);
	System.out.println("22/10: "+numbertwo/10);

	}

}
